# Lista (a)
lista_num=[5,8,2,9,1]

# Crescente (b)

lista_num.sort()


print(lista_num)

# Decrescente (b)

lista_num.reverse()

print(lista_num)

# Adicionando na lista (c)

lista_num.append(7)

lista_num.insert(2,3)

print(lista_num)

# removendo e adcionando em posição específica (d) 

lista_num.remove(9)

lista_num.insert(1,10)

print(lista_num)

# Excluindo da posição 2 até o 4 (e)

lista_num.pop(2)

lista_num.pop(3)

lista_num.pop(4)

print(lista_num)